        [TestMethod()]
        public void scalarMultiply_1x1()
        {
            double[,] data = new[,]
            {
                { 1.0}
            };

            Matrix test = new Matrix(data);
            double scalar = 10.0;

            test = test.scalarMultiply(scalar);

            Assert.IsTrue(test.rows == 1);
            Assert.IsTrue(test.cols == 1);
            Assert.IsTrue(test.data[0, 0] == 10);
        }

